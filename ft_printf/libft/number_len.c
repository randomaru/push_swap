#include "libft.h"

int		number_len(int n)
{
	int len;

	len = 1;
	if (n == INT_MIN)
		len = 10;
	else if (n < 0)
		n = -n;
	while (n > 9)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
